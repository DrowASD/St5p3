def investment_scenario(X, A, B):
  """
  Определяет сценарий инвестирования в стартап.

  Args:
    X: Минимальная сумма инвестиций.
    A: Сумма денег у Майкла.
    B: Сумма денег у Ивана.

  Returns:
    2: Если оба могут вложиться.
    Mike: Если только Майкл может вложиться.
    Ivan: Если только Иван может вложиться.
    1: Если оба вместе могут вложиться, но по отдельности нет.
    0: Если никто не может вложиться.
  """

  if A >= X and B >= X:
    return 2  # Оба могут вложиться
  elif A >= X:
    return "Mike"  # Только Майкл может вложиться
  elif B >= X:
    return "Ivan"  # Только Иван может вложиться
  elif A + B >= X:
    return 1  # Оба вместе могут вложиться
  else:
    return 0  # Никто не может вложиться

# Запрашиваем данные у пользователя
X = int(input("Введите минимальную сумму инвестиций: "))
A = int(input("Введите сумму денег у Майкла: "))
B = int(input("Введите сумму денег у Ивана: "))

# Вызываем функцию и выводим результат
result = investment_scenario(X, A, B)

# Выводим результат в более понятной форме
if result == 2:
  print("Оба инвестора могут вложиться.")
elif result == "Mike":
  print("Только Майкл может вложиться.")
elif result == "Ivan":
  print("Только Иван может вложиться.")
elif result == 1:
  print("Оба инвестора вместе могут вложиться.")
else:
  print("Никто не может вложиться.")